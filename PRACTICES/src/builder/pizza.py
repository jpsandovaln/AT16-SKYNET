class Pizza:
    def __init__(self):
        self.dough = ""
        self.sauce = ""
        self.cheese = ""

        self.ham = ""
        self.pineapple = ""

        self.olive = ""
        self.basil = ""
        self.tomato = ""
        self.corn = ""
        self.meat = ""

    def get_dough(self):
        return self.dough

    def set_dough(self, dough):
        self.dough = dough

    def get_sauce(self):
        return self.sauce

    def set_sauce(self, sauce):
        self.sauce = sauce

    def get_cheese(self):
        return self.cheese

    def set_cheese(self, cheese):
        self.cheese = cheese

    def get_ham(self):
        return self.ham

    def set_ham(self, ham):
        self.ham = ham

    def get_pineapple(self):
        return self.pineapple

    def set_pineapple(self, pineapple):
        self.pineapple = pineapple

    def get_olive(self):
        return self.olive

    def set_olive(self, olive):
        self.olive = olive

    def get_basil(self):
        return self.basil

    def set_basil(self, basil):
        self.basil = basil

    def get_tomato(self):
        return self.tomato

    def set_tomato(self, tomato):
        self.tomato = tomato

    def get_corn(self):
        return self.corn

    def set_corn(self, corn):
        self.corn = corn

    def get_meat(self):
        return self.meat

    def set_meat(self, meat):
        self.meat = meat
